void DuckDBPyConnection::Close() {
	result = nullptr;
	connection = nullptr;
	database = nullptr;
	temporary_views.clear();
	// https://peps.python.org/pep-0249/#Connection.close
	for (auto &cur : cursors) {
		auto cursor = cur.lock();
		if (!cursor) {
			// The cursor has already been closed
			continue;
		}
		cursor->Close();
	}
	registered_functions.clear();
	cursors.clear();
}

