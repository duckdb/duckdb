# Generates a C++ header embedding the HTML query-plan viewer template as a byte array.
# The template is kept as a real .html file (easy to edit/lint); embedding it as a byte array (rather than a string
# literal) keeps it within the per-literal length limits that MSVC and the C++ standard impose.
# Run via `make generate-files`. Edit the .html source, not the generated header.
import os

SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
ROOT = os.path.dirname(SCRIPT_DIR)
SRC = os.path.join(ROOT, 'src', 'common', 'tree_renderer', 'html_template.html')
DST = os.path.join(ROOT, 'src', 'include', 'duckdb', 'common', 'tree_renderer', 'html_template.hpp')


def to_byte_array(data):
    lines = []
    for i in range(0, len(data), 20):
        lines.append("    " + ", ".join(str(b) for b in data[i : i + 20]) + ",")
    return "\n".join(lines)


def main():
    with open(SRC, 'rb') as f:
        data = bytearray(f.read())
    data.append(0)  # null terminator so the array can be used directly as a C string
    content = """//===----------------------------------------------------------------------===//
// This file is automatically generated by scripts/generate_html_template.py
// Do not edit this file manually - edit src/common/tree_renderer/html_template.html and re-run `make generate-files`
//===----------------------------------------------------------------------===//

#pragma once

namespace duckdb {

//! Single-page interactive HTML viewer for query plans/profiles; the plan JSON is injected in place of __PLAN_JSON__.
//! Embedded as a byte array (not a string literal) to stay within the per-literal limits MSVC and the standard impose.
static const unsigned char HTML_TEMPLATE[] = {
%s
};

} // namespace duckdb
""" % to_byte_array(
        data
    )
    with open(DST, 'w', newline='\n') as f:
        f.write(content)
    print("generate_html_template.py: wrote %s (%d bytes embedded)" % (DST, len(data)))


if __name__ == '__main__':
    main()
