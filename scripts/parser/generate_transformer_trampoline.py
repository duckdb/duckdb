def finalize_name(rule_name):
    return f"Finalize{rule_name}Trampoline"


class ManualTransformRegistry:
    def __init__(self, paths):
        self.signatures = {}
        self._load(paths)

    def _load(self, paths):
        pattern = re.compile(r"\bPEGTransformerFactory::Transform([A-Za-z0-9_]+)\s*\(")
        for path in paths:
            text = path.read_text()
            for match in pattern.finditer(text):
                paren_start = text.index("(", match.start())
                depth = 0
                paren_end = None
                for offset, char in enumerate(text[paren_start:]):
                    if char == "(":
                        depth += 1
                    elif char == ")":
                        depth -= 1
                        if depth == 0:
                            paren_end = paren_start + offset
                            break
                if paren_end is None:
                    continue
                next_token = text[paren_end + 1 :].lstrip()
                if not next_token.startswith("{"):
                    continue
                line_start = text.rfind("\n", 0, match.start()) + 1
                signature = text[line_start : paren_end + 1]
                rule_name = match.group(1)
                self.signatures.setdefault(rule_name, set()).add(self._signature_key(signature))

    @staticmethod
    def _signature_key(signature):
        prefix = "PEGTransformerFactory::"
        signature = _norm_ws(signature)
        if prefix in signature:
            return signature[signature.find(prefix) :]
        return signature

    def has_rule(self, rule_name):
        return rule_name in self.signatures

    def has_no_arg_rule(self, rule_name):
        pattern = re.compile(rf"^PEGTransformerFactory::Transform{re.escape(rule_name)}\(PEGTransformer\s*&\s*\w+\)$")
        return any(pattern.match(signature) for signature in self.signatures.get(rule_name, ()))

    def has_parse_result_rule(self, rule_name):
        pattern = re.compile(
            rf"^PEGTransformerFactory::Transform{re.escape(rule_name)}"
            rf"\(PEGTransformer\s*&\s*\w+,\s*(?:const\s+)?ParseResult\s*&\s*\w+\)$"
        )
        return any(pattern.match(signature) for signature in self.signatures.get(rule_name, ()))

    def signature_status(self, rule_name, expected_signature):
        expected = self._signature_key(expected_signature)
        signatures = self.signatures.get(rule_name)
        if not signatures:
            return "missing"
        if expected in signatures:
            return "implemented"
        return "mismatched"


_manual_transform_registry = None


def manual_transform_registry():
    global _manual_transform_registry
    if _manual_transform_registry is None:
        _manual_transform_registry = ManualTransformRegistry(transformer_dir.glob("transform_*.cpp"))
    return _manual_transform_registry


def manual_body_exists(rule_name):
    return manual_transform_registry().has_rule(rule_name)


def no_arg_manual_body_exists(rule_name):
    return manual_transform_registry().has_no_arg_rule(rule_name)


def parse_result_manual_body_exists(rule_name):
    return manual_transform_registry().has_parse_result_rule(rule_name)


def discover_grammar_files():
    return sorted(path.name for path in statements_dir.glob("*.gram"))


def load_grammar_rules(gram_files):
    rules = {}
    for gram_file in gram_files:
        gram_path = statements_dir / gram_file
        try:
            parsed_rules = parse_peg_grammar(gram_path.read_text())
        except Exception as e:
            raise Exception(f"{gram_file}: {e}") from None
        for rule_name, rule in parsed_rules.items():
            if rule_name in rules:
                raise Exception(f"duplicate grammar rule '{rule_name}' in {gram_file}")
            rules[rule_name] = rule
    return rules


def validate_manual_transform_signatures(gram_files, grammar_types_file, rule_types, excluded_rules):
    matcher_rule_names = set(load_matcher_rule_overrides(grammar_types_file).keys())
    identifier_override_rules = load_identifier_override_rules(grammar_types_file)
    results = [
        process_gram_file(gram_file, rule_types, excluded_rules, matcher_rule_names, identifier_override_rules)
        for gram_file in gram_files
    ]

    pending_stubs = []
    sig_mismatches = []
    registry = manual_transform_registry()
    for result in results:
        if not result.body_stubs:
            continue
        for rule_name, stub in result.body_stubs:
            expected_sig = stub.split("\n")[0].rstrip("{").rstrip()
            status = registry.signature_status(rule_name, expected_sig)
            if status == "implemented":
                continue
            if status == "mismatched":
                sig_mismatches.append((result.gram_stem, rule_name, expected_sig))
            else:
                pending_stubs.append((result.gram_stem, rule_name, expected_sig))

    if not pending_stubs and not sig_mismatches:
        return

    print("Error: manual transformer function signatures do not match generated grammar metadata:", file=sys.stderr)
    if sig_mismatches:
        print("  Signature mismatches:", file=sys.stderr)
        for gram_stem, rule_name, expected_sig in sig_mismatches:
            print(f"    transform_{gram_stem}.cpp: Transform{rule_name} -- expected: {expected_sig}", file=sys.stderr)
    if pending_stubs:
        print("  Missing manual transform functions:", file=sys.stderr)
        for gram_stem, rule_name, expected_sig in pending_stubs:
            print(f"    transform_{gram_stem}.cpp: Transform{rule_name} -- expected: {expected_sig}", file=sys.stderr)
    sys.exit(1)


def load_matcher_overrides(grammar_types_file):
    result = {}
    for rule_name, override in load_matcher_rule_overrides(grammar_types_file).items():
        matcher = override.get("matcher")
        matcher_override = MatcherOverride(rule_name, matcher)
        if matcher_override.is_identifier() or matcher_override.is_terminal():
            result[rule_name] = matcher_override
    return result


def typed_result_expr(cpp_type, expr, by_value):
    move_expr = expr if by_value else expr
    if by_value:
        move_expr = f"std::move({expr})"
    return f"make_uniq<TypedTransformResult<{cpp_type}>>({move_expr})"


class RuleCapabilityStatus(Enum):
    EXCLUDED = "excluded"
    GENERATED = "generated"
    MANUAL = "manual"
    MANUAL_FINALIZE = "manual_finalize"
    PROVIDED = "provided"
    SYNTAX_ONLY = "syntax_only"
    UNSUPPORTED = "unsupported"


@dataclass
class RuleCapability:
    rule_name: str
    status: RuleCapabilityStatus
    reason: str = ""
    ast: object = None


class UseGramPreviewEmitter:
    def __init__(self, rules, rule_types, excluded_rules, matcher_overrides, rule_config):
        self.rules = rules
        self.rule_types = rule_types
        self.excluded_rules = excluded_rules
        self.matcher_overrides = matcher_overrides
        self.rule_config = rule_config
        self.syntax_only_rules = self.collect_syntax_only_rules()
        self.rule_capabilities = self.collect_rule_capabilities()

    def collect_syntax_only_rules(self):
        result = {rule_name for rule_name in self.excluded_rules if rule_name not in self.rule_types}
        parsed_rules = {
            rule_name: tokens_to_ast(rule.tokens)
            for rule_name, rule in self.rules.items()
            if rule_name not in self.matcher_overrides
            and rule_name not in self.excluded_rules
            and not rule_name.startswith("%")
            and rule_name not in INTERNAL_GRAMMAR_RULES
        }
        changed = True
        while changed:
            changed = False
            for rule_name, ast in parsed_rules.items():
                if rule_name in result:
                    continue
                if rule_name in self.matcher_overrides:
                    continue
                if rule_name not in self.rule_types and is_syntax_only_rule_body(ast, result):
                    result.add(rule_name)
                    changed = True
        return result

    def collect_rule_capabilities(self):
        return {rule_name: self.classify_rule(rule_name, rule) for rule_name, rule in self.rules.items()}

    def cpp_type(self, rule_name):
        return self.rule_types[rule_name].cpp_type

    def by_value(self, rule_name):
        return self.rule_types[rule_name].by_value

    def rule_config_entry(self, rule_name):
        return self.rule_config.get(rule_name)

    def is_excluded_rule(self, rule_name):
        config = self.rule_config_entry(rule_name)
        return config is not None and config.mode == TrampolineRuleMode.EXCLUDED

    def is_manual_rule(self, rule_name):
        config = self.rule_config_entry(rule_name)
        return config is not None and config.mode == TrampolineRuleMode.MANUAL

    def is_manual_finalize_rule(self, rule_name):
        config = self.rule_config_entry(rule_name)
        return config is not None and config.mode == TrampolineRuleMode.MANUAL_FINALIZE

    def is_forward_rule(self, rule_name):
        config = self.rule_config_entry(rule_name)
        return config is not None and config.mode == TrampolineRuleMode.FORWARD

    def emitted_rules(self):
        return [
            rule_name
            for rule_name, capability in self.rule_capabilities.items()
            if capability.status
            in (
                RuleCapabilityStatus.GENERATED,
                RuleCapabilityStatus.MANUAL,
                RuleCapabilityStatus.MANUAL_FINALIZE,
            )
        ]

    def emitted_ops_rules(self):
        return list(SPECIAL_DISPATCH_RULES.keys()) + self.emitted_rules()

    def initialize_hook(self, rule_name):
        config = self.rule_config_entry(rule_name)
        if config is not None and config.init:
            return config.init
        return init_name(rule_name)

    def finalize_hook(self, rule_name):
        config = self.rule_config_entry(rule_name)
        if config is not None and config.finalize:
            return config.finalize
        return finalize_name(rule_name)

    def emit_header_declarations(self):
        lines = []
        for rule_name in SPECIAL_DISPATCH_RULES:
            lines.append(
                f"\tstatic void {init_name(rule_name)}(PEGTransformer &transformer, TransformStack &stack, "
                f"TransformStackFrame &frame);\n"
            )
            lines.append(
                f"\tstatic unique_ptr<TransformResultValue> {finalize_name(rule_name)}(PEGTransformer &transformer, "
                f"TransformStack &stack, TransformStackFrame &frame);\n"
            )
        for rule_name in self.emitted_rules():
            if self.is_manual_rule(rule_name):
                continue
            lines.append(
                f"\tstatic void {self.initialize_hook(rule_name)}(PEGTransformer &transformer, TransformStack &stack, "
                f"TransformStackFrame &frame);\n"
            )
            lines.append(
                f"\tstatic unique_ptr<TransformResultValue> {self.finalize_hook(rule_name)}(PEGTransformer &transformer, "
                f"TransformStack &stack, TransformStackFrame &frame);\n"
            )
        return "".join(lines)

    def classify_rule(self, rule_name, rule):
        if self.is_excluded_rule(rule_name):
            return RuleCapability(rule_name, RuleCapabilityStatus.EXCLUDED)
        if self.is_manual_rule(rule_name):
            return RuleCapability(rule_name, RuleCapabilityStatus.MANUAL)
        if rule_name.startswith("%"):
            return RuleCapability(rule_name, RuleCapabilityStatus.PROVIDED, "internal matcher rule")
        if rule_name in INTERNAL_GRAMMAR_RULES:
            return RuleCapability(rule_name, RuleCapabilityStatus.PROVIDED, "internal matcher rule")
        if rule_name in self.matcher_overrides and rule_name in self.rule_types:
            try:
                self.emit_matcher_override_rule(rule_name)
            except NotImplementedError as e:
                return RuleCapability(rule_name, RuleCapabilityStatus.UNSUPPORTED, str(e))
            return RuleCapability(rule_name, RuleCapabilityStatus.GENERATED, "provided by matcher_rule_overrides")
        if rule_name in self.matcher_overrides:
            return RuleCapability(rule_name, RuleCapabilityStatus.PROVIDED, "provided by matcher_rule_overrides")
        if rule_name in self.excluded_rules:
            return RuleCapability(rule_name, RuleCapabilityStatus.EXCLUDED, "excluded by grammar_types.yml")
        try:
            ast = tokens_to_ast(rule.tokens)
        except Exception as e:
            return RuleCapability(rule_name, RuleCapabilityStatus.UNSUPPORTED, f"AST parse error: {e}")
        if is_syntax_only_rule_body(ast, self.syntax_only_rules) and rule_name not in self.rule_types:
            return RuleCapability(rule_name, RuleCapabilityStatus.SYNTAX_ONLY, ast=ast)
        if literal_string_values(ast) is None and rule_name not in self.rule_types:
            return RuleCapability(
                rule_name, RuleCapabilityStatus.UNSUPPORTED, "no return type in grammar_types.yml", ast
            )
        if self.is_manual_finalize_rule(rule_name):
            try:
                self.emit_initialize_rule(rule_name, ast)
            except NotImplementedError as e:
                return RuleCapability(
                    rule_name,
                    RuleCapabilityStatus.UNSUPPORTED,
                    f"manual_finalize initialize: {e}",
                    ast,
                )
            return RuleCapability(rule_name, RuleCapabilityStatus.MANUAL_FINALIZE, ast=ast)
        try:
            self.emit_rule(rule_name, ast)
        except KeyError as e:
            return RuleCapability(
                rule_name,
                RuleCapabilityStatus.UNSUPPORTED,
                f"child rule '{e.args[0]}' is missing from grammar_types.yml",
                ast,
            )
        except NotImplementedError as e:
            return RuleCapability(rule_name, RuleCapabilityStatus.UNSUPPORTED, str(e), ast)
        return RuleCapability(rule_name, RuleCapabilityStatus.GENERATED, ast=ast)

    def rule_status(self, rule_name, rule):
        capability = self.rule_capabilities[rule_name]
        return capability.status.value, capability.reason

    def emit_report(self):
        rows = []
        counts = {}
        for rule_name, capability in self.rule_capabilities.items():
            status = capability.status.value
            reason = capability.reason
            counts[status] = counts.get(status, 0) + 1
            rows.append((rule_name, status, reason))

        lines = []
        summary = ", ".join(f"{status}={counts[status]}" for status in sorted(counts))
        lines.append(f"summary: {summary}")
        for rule_name, status, reason in rows:
            suffix = f": {reason}" if reason else ""
            lines.append(f"{rule_name}: {status}{suffix}")
        return "\n".join(lines)

    def missing_grammar_type_errors(self):
        result = []
        for capability in self.rule_capabilities.values():
            if capability.status != RuleCapabilityStatus.UNSUPPORTED:
                continue
            if "grammar_types.yml" not in capability.reason:
                continue
            result.append(capability)
        return result

    def validate_grammar_types(self):
        missing = self.missing_grammar_type_errors()
        if not missing:
            return
        print("Error: missing trampoline transformer metadata in grammar_types.yml:", file=sys.stderr)
        for capability in missing:
            print(f"  {capability.rule_name}: {capability.reason}", file=sys.stderr)
        print("Add type metadata for these rules or explicitly exclude syntax-only rules.", file=sys.stderr)
        sys.exit(1)

    def emit_source(self):
        lines = []
        lines.append(GENERATED_HEADER)
        lines.append('\n#include "duckdb/parser/peg/transformer/peg_transformer.hpp"\n\n')
        lines.append("namespace duckdb {\n\n")
        lines.append("")
        for rule_name in self.emitted_ops_rules():
            lines.append(
                f"static const TransformFrameOps {ops_name(rule_name)} = "
                f'{{"{rule_name}", &PEGTransformerFactory::{self.initialize_hook(rule_name)}, '
                f"&PEGTransformerFactory::{self.finalize_hook(rule_name)}}};"
            )
        lines.append("")
        lines.extend(self.emit_ops_lookup())
        lines.append("")
        lines.extend(self.emit_special_dispatch_rules())
        lines.append("")
        for rule_name, rule in self.rules.items():
            capability = self.rule_capabilities[rule_name]
            if capability.status not in (RuleCapabilityStatus.GENERATED, RuleCapabilityStatus.MANUAL_FINALIZE):
                continue
            if rule_name in self.matcher_overrides:
                lines.extend(self.emit_rule(rule_name, None))
                lines.append("")
                continue
            lines.extend(self.emit_rule(rule_name, tokens_to_ast(rule.tokens)))
            lines.append("")
        lines.append("} // namespace duckdb")
        return "\n".join(lines)

    def emit(self):
        lines = []
        lines.append("// Trampoline transformer generation")
        lines.append("// AUTO-GENERATED by scripts/parser/generate_transformer_trampoline.py")
        lines.append("")
        lines.append("// Header declarations for PEGTransformerFactory:")
        lines.append(self.emit_header_declarations().rstrip())
        lines.append("")
        lines.append("// Source definitions:")
        lines.append(self.emit_source())
        return "\n".join(lines)

    def emit_ops_lookup(self):
        lines = []
        lines.append(
            "const case_insensitive_map_t<const TransformFrameOps *> &PEGTransformerFactory::GeneratedTrampolineOps() {"
        )
        lines.append("\tstatic const case_insensitive_map_t<const TransformFrameOps *> result = {")
        for rule_name in self.emitted_ops_rules():
            lines.append(f'\t    {{"{rule_name}", &{ops_name(rule_name)}}},')
        lines.append("\t};")
        lines.append("\treturn result;")
        lines.append("}")
        return lines

    def emit_special_dispatch_rules(self):
        lines = []
        for rule_name, dispatch in SPECIAL_DISPATCH_RULES.items():
            cpp_type = dispatch["cpp_type"]
            lines.append(
                f"void PEGTransformerFactory::{init_name(rule_name)}(PEGTransformer &transformer, TransformStack &stack, "
                f"TransformStackFrame &frame) {{"
            )
            lines.append("\tframe.ReserveChildSlots(0);")
            lines.append("}")
            lines.append("")
            lines.append(
                f"unique_ptr<TransformResultValue> PEGTransformerFactory::{finalize_name(rule_name)}(PEGTransformer &transformer, "
                f"TransformStack &stack, TransformStackFrame &frame) {{"
            )
            lines.append(f"\tauto result = {dispatch['transform']};")
            lines.append(f"\treturn {typed_result_expr(cpp_type, 'result', True)};")
            lines.append("}")
            lines.append("")
        return lines

    def emit_rule(self, rule_name, ast):
        if rule_name in self.matcher_overrides:
            return self.emit_matcher_override_rule(rule_name)
        if self.is_forward_rule(rule_name):
            return self.emit_forward_rule(rule_name, ast)
        if (
            literal_string_values(ast) is not None
            or self.is_no_arg_syntax_only_rule(rule_name, ast)
            or self.is_parse_result_syntax_only_rule(rule_name, ast)
        ):
            return self.emit_syntax_only_rule(rule_name, ast)
        if isinstance(ast, ChoiceNode):
            if self.is_manual_finalize_rule(rule_name):
                raise NotImplementedError("manual_finalize choice rules are not currently supported")
            return self.emit_choice_rule(rule_name, ast)
        if isinstance(ast, SequenceNode):
            return self.emit_sequence_rule(rule_name, ast)
        if isinstance(ast, ReferenceNode):
            return self.emit_sequence_rule(rule_name, SequenceNode([ast]))
        if isinstance(ast, ParensNode):
            return self.emit_sequence_rule(rule_name, SequenceNode([ast]))
        if isinstance(ast, ListMacroNode):
            return self.emit_sequence_rule(rule_name, SequenceNode([ast]))
        if isinstance(ast, RepeatNode):
            return self.emit_sequence_rule(rule_name, SequenceNode([ast]))
        if isinstance(ast, OptionalNode):
            return self.emit_sequence_rule(rule_name, SequenceNode([ast]))
        raise NotImplementedError(f"unsupported preview shape for {rule_name}: {type(ast).__name__}")

    def emit_matcher_override_rule(self, rule_name):
        result_expr = self.matcher_transform_expr(rule_name, "frame.parse_result")
        lines = []
        lines.append(
            f"void PEGTransformerFactory::{init_name(rule_name)}(PEGTransformer &transformer, TransformStack &stack, "
            f"TransformStackFrame &frame) {{"
        )
        lines.append("\tframe.ReserveChildSlots(0);")
        lines.append("}")
        lines.append("")
        lines.append(
            f"unique_ptr<TransformResultValue> PEGTransformerFactory::{finalize_name(rule_name)}(PEGTransformer &transformer, "
            f"TransformStack &stack, TransformStackFrame &frame) {{"
        )
        lines.append(f"\tauto result = {result_expr};")
        lines.append(f"\treturn {typed_result_expr(self.cpp_type(rule_name), 'result', self.by_value(rule_name))};")
        lines.append("}")
        return lines

    def is_no_arg_syntax_only_rule(self, rule_name, ast):
        return (
            rule_name in self.rule_types
            and no_arg_manual_body_exists(rule_name)
            and is_syntax_only_rule_body(ast, self.syntax_only_rules)
        )

    def is_parse_result_syntax_only_rule(self, rule_name, ast):
        return (
            rule_name in self.rule_types
            and parse_result_manual_body_exists(rule_name)
            and not isinstance(ast, ChoiceNode)
        )

    def is_parse_result_manual_choice_rule(self, rule_name, ast):
        return (
            rule_name in self.rule_types and parse_result_manual_body_exists(rule_name) and isinstance(ast, ChoiceNode)
        )

    def choice_syntax_only_alternative_names(self, ast):
        return [
            alternative.name
            for alternative in ast.alternatives
            if isinstance(alternative, ReferenceNode) and alternative.name in self.syntax_only_rules
        ]

    def emit_initialize_rule(self, rule_name, ast):
        if (
            self.is_no_arg_syntax_only_rule(rule_name, ast)
            or self.is_parse_result_syntax_only_rule(rule_name, ast)
            or literal_string_values(ast) is not None
        ):
            return self.emit_syntax_only_initialize(rule_name)
        if isinstance(ast, ChoiceNode):
            return self.emit_choice_initialize(rule_name, ast)
        if isinstance(ast, SequenceNode):
            return self.emit_sequence_initialize(rule_name, ast)
        if isinstance(ast, ReferenceNode):
            return self.emit_sequence_initialize(rule_name, SequenceNode([ast]))
        if isinstance(ast, ParensNode):
            return self.emit_sequence_initialize(rule_name, SequenceNode([ast]))
        if isinstance(ast, ListMacroNode):
            return self.emit_sequence_initialize(rule_name, SequenceNode([ast]))
        if isinstance(ast, RepeatNode):
            return self.emit_sequence_initialize(rule_name, SequenceNode([ast]))
        if isinstance(ast, OptionalNode):
            return self.emit_sequence_initialize(rule_name, SequenceNode([ast]))
        raise NotImplementedError(f"unsupported preview shape for {rule_name}: {type(ast).__name__}")

    def identifier_var_name(self, rule_name):
        return to_snake_case(rule_name)

    def transform_arg_expr(self, rule_name, var_name):
        if self.by_value(rule_name):
            return f"std::move({var_name})"
        return var_name

    def direct_matcher_cpp_type(self, rule_name):
        matcher = self.matcher_overrides[rule_name].matcher
        if matcher in ("identifier", "reserved_identifier"):
            return "Identifier"
        return self.cpp_type(rule_name)

    def direct_matcher_value_expr(self, rule_name, parse_expr):
        matcher = self.matcher_overrides[rule_name].matcher
        if matcher in ("identifier", "reserved_identifier"):
            return f"{parse_expr}.Cast<IdentifierParseResult>().identifier"
        return self.matcher_transform_expr(rule_name, parse_expr)

    def is_identifier_matcher(self, rule_name):
        return self.matcher_overrides[rule_name].matcher in ("identifier", "reserved_identifier")

    def direct_matcher_vector_push_expr(self, rule_name, var_name):
        if self.is_identifier_matcher(rule_name):
            return var_name
        if self.by_value(rule_name):
            return f"std::move({var_name})"
        return var_name

    def dynamic_child(self, plan):
        return plan.repeat_child or plan.optional_list_child or plan.required_repeat_child or plan.list_child

    def adjusted_slot_expr(self, plan, slot_idx):
        dynamic_child = self.dynamic_child(plan)
        if dynamic_child and slot_idx > dynamic_child.slot_start:
            return f"{slot_idx} + dynamic_child_count - 1"
        return str(slot_idx)

    def emit_stack_child_push(self, lines, plan, stack_child, indent="\t"):
        if isinstance(stack_child, OptionalStackChild):
            slot_expr = self.adjusted_slot_expr(plan, stack_child.slot_idx)
            lines.append(
                f"{indent}auto &{stack_child.var_name}_opt = {stack_child.parse_expr}.Cast<OptionalParseResult>();"
            )
            lines.append(f"{indent}if ({stack_child.var_name}_opt.HasResult()) {{")
            child_expr = stack_child.result_expr_template.format(opt=f"{stack_child.var_name}_opt")
            lines.append(
                f"{indent}\tstack.PushFrame({child_expr}, {ops_name(stack_child.rule_name)}, "
                f"TransformFrameResultTarget(frame.frame_index, {slot_expr}));"
            )
            lines.append(f"{indent}}}")
        else:
            slot_expr = self.adjusted_slot_expr(plan, stack_child.slot_idx)
            lines.append(
                f"{indent}stack.PushFrame({stack_child.parse_expr}, {ops_name(stack_child.rule_name)}, "
                f"TransformFrameResultTarget(frame.frame_index, {slot_expr}));"
            )

    def emit_dynamic_child_count(self, plan):
        dynamic_child = self.dynamic_child(plan)
        if not dynamic_child:
            return []
        lines = []
        if plan.list_child:
            list_child = plan.list_child
            lines.append(f"\tauto dynamic_list_items = ExtractParseResultsFromList({list_child.parse_expr});")
            lines.append("\tauto dynamic_child_count = dynamic_list_items.size();")
        elif plan.optional_list_child:
            list_child = plan.optional_list_child
            lines.append("\tidx_t dynamic_child_count = 0;")
            lines.append(f"\tauto &dynamic_list_opt = {list_child.parse_expr}.Cast<OptionalParseResult>();")
            lines.append("\tif (dynamic_list_opt.HasResult()) {")
            child_expr = list_child.result_expr_template.format(opt="dynamic_list_opt")
            lines.append(f"\t\tauto dynamic_list_items = ExtractParseResultsFromList({child_expr});")
            lines.append("\t\tdynamic_child_count = dynamic_list_items.size();")
            lines.append("\t}")
        elif plan.required_repeat_child:
            repeat_child = plan.required_repeat_child
            lines.append(f"\tauto &dynamic_repeat_pr = {repeat_child.parse_expr}.Cast<RepeatParseResult>();")
            lines.append("\tauto dynamic_repeat_children = dynamic_repeat_pr.GetChildren();")
            lines.append("\tauto dynamic_child_count = dynamic_repeat_children.size();")
        else:
            repeat_child = plan.repeat_child
            lines.append("\tidx_t dynamic_child_count = 0;")
            lines.append(f"\tauto &dynamic_repeat_opt = {repeat_child.parse_expr}.Cast<OptionalParseResult>();")
            lines.append("\tif (dynamic_repeat_opt.HasResult()) {")
            lines.append("\t\tauto &dynamic_repeat_pr = dynamic_repeat_opt.GetResult().Cast<RepeatParseResult>();")
            lines.append("\t\tauto dynamic_repeat_children = dynamic_repeat_pr.GetChildren();")
            lines.append("\t\tdynamic_child_count = dynamic_repeat_children.size();")
            lines.append("\t}")
        return lines

    def sequence_forward_child(self, rule_name, plan):
        semantic_args = [arg for arg in plan.finalize_args if isinstance(arg, StackChild)]
        if len(plan.finalize_args) != 1 or len(semantic_args) != 1:
            return None
        child_arg = semantic_args[0]
        if child_arg.rule_name not in self.rule_types:
            return None
        if self.cpp_type(child_arg.rule_name) != self.cpp_type(rule_name):
            return None
        return child_arg

    def auto_sequence_forward_child(self, rule_name, plan):
        if manual_body_exists(rule_name) or self.rule_types[rule_name].pass_location:
            return None
        return self.sequence_forward_child(rule_name, plan)

    def emit_sequence_forward_finalize_body(self, rule_name, plan, child_arg):
        cpp_type = self.cpp_type(rule_name)
        by_value = self.by_value(rule_name)
        slot_expr = self.adjusted_slot_expr(plan, child_arg.slot_idx)
        return [
            f"\tauto result = frame.TakeResult<{cpp_type}>({slot_expr});",
            f"\treturn {typed_result_expr(cpp_type, 'result', by_value)};",
            "}",
        ]

    def emit_forward_rule(self, rule_name, ast):
        sequence_ast = ast if isinstance(ast, SequenceNode) else SequenceNode([ast])
        plan = plan_trampoline_sequence_rule_with_terminals(
            sequence_ast,
            self.matcher_overrides,
            self.syntax_only_rules,
            allow_dynamic_stack_followers=True,
            allow_top_level_repeat=True,
        )
        child_arg = self.sequence_forward_child(rule_name, plan)
        if child_arg is None:
            raise NotImplementedError("forward rules must have exactly one stack child")

        lines = self.emit_sequence_initialize(rule_name, sequence_ast)
        lines.append("")
        lines.append(
            f"unique_ptr<TransformResultValue> PEGTransformerFactory::{finalize_name(rule_name)}(PEGTransformer &transformer, "
            f"TransformStack &stack, TransformStackFrame &frame) {{"
        )
        lines.extend(self.emit_sequence_forward_finalize_body(rule_name, plan, child_arg))
        return lines

    def emit_syntax_only_rule(self, rule_name, ast):
        lines = self.emit_syntax_only_initialize(rule_name)
        if self.is_manual_finalize_rule(rule_name):
            return lines
        cpp_type = self.cpp_type(rule_name)
        by_value = self.by_value(rule_name)
        literal_values = literal_string_values(ast)
        lines.append("")
        lines.append(
            f"unique_ptr<TransformResultValue> PEGTransformerFactory::{finalize_name(rule_name)}(PEGTransformer &transformer, "
            f"TransformStack &stack, TransformStackFrame &frame) {{"
        )
        if self.is_parse_result_syntax_only_rule(rule_name, ast):
            if isinstance(ast, ChoiceNode):
                lines.append("\tauto &list_pr = frame.parse_result.Cast<ListParseResult>();")
                lines.append("\tauto &choice_pr = list_pr.Child<ChoiceParseResult>(0);")
                lines.append(f"\tauto result = Transform{rule_name}(transformer, choice_pr.GetResult());")
            else:
                lines.append(f"\tauto result = Transform{rule_name}(transformer, frame.parse_result);")
        elif cpp_type == "string" and literal_values is not None and not manual_body_exists(rule_name):
            if len(literal_values) == 1:
                lines.append(f'\tstring result = "{literal_values[0]}";')
            else:
                lines.append("\tauto &list_pr = frame.parse_result.Cast<ListParseResult>();")
                lines.append("\tauto &choice_pr = list_pr.Child<ChoiceParseResult>(0);")
                lines.append("\tauto result = choice_pr.GetResult().Cast<KeywordParseResult>().keyword;")
        else:
            lines.append(f"\tauto result = Transform{rule_name}(transformer);")
        lines.append(f"\treturn {typed_result_expr(cpp_type, 'result', by_value)};")
        lines.append("}")
        return lines

    def emit_syntax_only_initialize(self, rule_name):
        lines = []
        lines.append(
            f"void PEGTransformerFactory::{init_name(rule_name)}(PEGTransformer &transformer, TransformStack &stack, "
            f"TransformStackFrame &frame) {{"
        )
        lines.append("\tframe.ReserveChildSlots(0);")
        lines.append("}")
        return lines

    def emit_choice_rule(self, rule_name, ast):
        lines = self.emit_choice_initialize(rule_name, ast)
        if self.is_manual_finalize_rule(rule_name):
            return lines
        cpp_type = self.cpp_type(rule_name)
        by_value = self.by_value(rule_name)
        syntax_only_alternatives = self.choice_syntax_only_alternative_names(ast)
        child_cpp_types = set()
        for alternative in ast.alternatives:
            if isinstance(alternative, ReferenceNode) and alternative.name in self.rule_types:
                child_cpp_types.add(self.cpp_type(alternative.name))
        has_string_child_manual_transform = (
            len(child_cpp_types) == 1
            and self.cpp_type(rule_name) not in child_cpp_types
            and "string" in child_cpp_types
            and manual_body_exists(rule_name)
        )
        lines.append("")
        lines.append(
            f"unique_ptr<TransformResultValue> PEGTransformerFactory::{finalize_name(rule_name)}(PEGTransformer &transformer, "
            f"TransformStack &stack, TransformStackFrame &frame) {{"
        )
        if self.is_parse_result_manual_choice_rule(rule_name, ast):
            lines.append("\tauto &list_pr = frame.parse_result.Cast<ListParseResult>();")
            lines.append("\tauto &choice_result = list_pr.Child<ChoiceParseResult>(0).GetResult();")
            lines.append(f"\t{cpp_type} result {{}};")
            lines.append("\tif (frame.child_results[0]) {")
            lines.append(f"\t\tresult = frame.TakeResult<{cpp_type}>(0);")
            lines.append("\t} else {")
            lines.append(f"\t\tresult = Transform{rule_name}(transformer, choice_result);")
            lines.append("\t}")
        elif len(child_cpp_types) == 1 and cpp_type not in child_cpp_types and manual_body_exists(rule_name):
            child_cpp_type = next(iter(child_cpp_types))
            if child_cpp_type == "string":
                lines.append("\tstring child;")
                lines.append("\tif (frame.child_results[0]) {")
                lines.append("\t\tchild = frame.TakeResult<string>(0);")
                lines.append("\t} else {")
                lines.append("\t\tauto &list_pr = frame.parse_result.Cast<ListParseResult>();")
                lines.append("\t\tauto &choice_result = list_pr.Child<ChoiceParseResult>(0).GetResult();")
                lines.append("\t\tif (choice_result.type == ParseResultType::IDENTIFIER) {")
                lines.append(
                    "\t\t\tchild = choice_result.Cast<IdentifierParseResult>().identifier.GetIdentifierName();"
                )
                lines.append("\t\t} else if (choice_result.type == ParseResultType::KEYWORD) {")
                lines.append("\t\t\tchild = choice_result.Cast<KeywordParseResult>().keyword;")
                lines.append("\t\t} else if (choice_result.type == ParseResultType::STRING) {")
                lines.append("\t\t\tchild = choice_result.Cast<StringLiteralParseResult>().result;")
                lines.append("\t\t} else if (choice_result.type == ParseResultType::OPERATOR) {")
                lines.append("\t\t\tchild = choice_result.Cast<OperatorParseResult>().operator_token;")
                lines.append("\t\t} else {")
                lines.append("\t\t\tchild = TransformIdentifierOrKeyword(transformer, choice_result);")
                lines.append("\t\t}")
                lines.append("\t}")
            else:
                lines.append(f"\tauto child = frame.TakeResult<{child_cpp_type}>(0);")
            child_arg = "std::move(child)" if child_cpp_type.startswith("unique_ptr<") else "child"
            lines.append(f"\tauto result = Transform{rule_name}(transformer, {child_arg});")
        elif cpp_type == "string":
            lines.append("\tstring result;")
            lines.append("\tif (frame.child_results[0]) {")
            lines.append("\t\tresult = frame.TakeResult<string>(0);")
            lines.append("\t} else {")
            lines.append("\t\tauto &list_pr = frame.parse_result.Cast<ListParseResult>();")
            lines.append("\t\tauto &choice_result = list_pr.Child<ChoiceParseResult>(0).GetResult();")
            lines.append("\t\tif (choice_result.type == ParseResultType::IDENTIFIER) {")
            lines.append("\t\t\tresult = choice_result.Cast<IdentifierParseResult>().identifier.GetIdentifierName();")
            lines.append("\t\t} else if (choice_result.type == ParseResultType::KEYWORD) {")
            lines.append("\t\t\tresult = choice_result.Cast<KeywordParseResult>().keyword;")
            lines.append("\t\t} else if (choice_result.type == ParseResultType::STRING) {")
            lines.append("\t\t\tresult = choice_result.Cast<StringLiteralParseResult>().result;")
            lines.append("\t\t} else if (choice_result.type == ParseResultType::OPERATOR) {")
            lines.append("\t\t\tresult = choice_result.Cast<OperatorParseResult>().operator_token;")
            lines.append("\t\t} else {")
            lines.append("\t\t\tresult = transformer.Transform<string>(choice_result);")
            lines.append("\t\t}")
            lines.append("\t}")
        elif cpp_type == "Identifier":
            lines.append("\tIdentifier result;")
            lines.append("\tif (frame.child_results[0]) {")
            lines.append("\t\tresult = frame.TakeResult<Identifier>(0);")
            lines.append("\t} else {")
            lines.append("\t\tauto &list_pr = frame.parse_result.Cast<ListParseResult>();")
            lines.append("\t\tauto &choice_result = list_pr.Child<ChoiceParseResult>(0).GetResult();")
            lines.append("\t\tif (choice_result.type == ParseResultType::IDENTIFIER) {")
            lines.append("\t\t\tresult = choice_result.Cast<IdentifierParseResult>().identifier;")
            lines.append("\t\t} else if (choice_result.type == ParseResultType::KEYWORD) {")
            lines.append("\t\t\tresult = Identifier(choice_result.Cast<KeywordParseResult>().keyword);")
            lines.append("\t\t} else if (choice_result.type == ParseResultType::STRING) {")
            lines.append("\t\t\tresult = Identifier(choice_result.Cast<StringLiteralParseResult>().result);")
            lines.append("\t\t} else {")
            lines.append("\t\t\tresult = Identifier(TransformIdentifierOrKeyword(transformer, choice_result));")
            lines.append("\t\t}")
            lines.append("\t}")
        elif syntax_only_alternatives:
            lines.append(f"\t{cpp_type} result {{}};")
            lines.append("\tif (frame.child_results[0]) {")
            lines.append(f"\t\tresult = frame.TakeResult<{cpp_type}>(0);")
            lines.append("\t}")
        else:
            lines.append(f"\tauto result = frame.TakeResult<{cpp_type}>(0);")
        lines.append(f"\treturn {typed_result_expr(cpp_type, 'result', by_value)};")
        lines.append("}")
        return lines

    def emit_choice_initialize(self, rule_name, ast):
        lines = []
        lines.append(
            f"void PEGTransformerFactory::{init_name(rule_name)}(PEGTransformer &transformer, TransformStack &stack, "
            f"TransformStackFrame &frame) {{"
        )
        lines.append("\tauto &list_pr = frame.parse_result.Cast<ListParseResult>();")
        lines.append("\tauto &choice_pr = list_pr.Child<ChoiceParseResult>(0);")
        lines.append("\tauto &choice_result = choice_pr.GetResult();")
        lines.append("\tframe.ReserveChildSlots(1);")
        lines.append("\tauto &ops_map = PEGTransformerFactory::GeneratedTrampolineOps();")
        lines.append("\tauto ops_entry = ops_map.find(choice_result.name);")
        syntax_only_alternatives = self.choice_syntax_only_alternative_names(ast)
        child_cpp_types = set()
        for alternative in ast.alternatives:
            if isinstance(alternative, ReferenceNode) and alternative.name in self.rule_types:
                child_cpp_types.add(self.cpp_type(alternative.name))
        has_string_child_manual_transform = (
            len(child_cpp_types) == 1
            and self.cpp_type(rule_name) not in child_cpp_types
            and "string" in child_cpp_types
            and manual_body_exists(rule_name)
        )
        if self.cpp_type(rule_name) == "Identifier":
            direct_string_names = [
                alternative.name
                for alternative in ast.alternatives
                if isinstance(alternative, ReferenceNode)
                and alternative.name in self.rule_types
                and self.cpp_type(alternative.name) == "string"
            ]
            direct_conditions = [
                "choice_result.name.empty()",
                "choice_result.type == ParseResultType::IDENTIFIER",
                "choice_result.type == ParseResultType::KEYWORD",
                "choice_result.type == ParseResultType::STRING",
            ]
            lines.append("\tif (" + " || ".join(direct_conditions) + ") {")
            lines.append("\t\treturn;")
            lines.append("\t}")
            if direct_string_names:
                direct_string_conditions = [f'choice_result.name == "{name}"' for name in direct_string_names]
                lines.append("\tif (ops_entry == ops_map.end() && (" + " || ".join(direct_string_conditions) + ")) {")
                lines.append("\t\treturn;")
                lines.append("\t}")
        if self.cpp_type(rule_name) == "string":
            direct_conditions = [
                "choice_result.type == ParseResultType::IDENTIFIER",
                "choice_result.type == ParseResultType::KEYWORD",
                "choice_result.type == ParseResultType::STRING",
                "choice_result.type == ParseResultType::OPERATOR",
            ]
            lines.append("\tif (" + " || ".join(direct_conditions) + ") {")
            lines.append("\t\treturn;")
            lines.append("\t}")
        if syntax_only_alternatives:
            syntax_only_conditions = [f'choice_result.name == "{name}"' for name in syntax_only_alternatives]
            lines.append("\tif (ops_entry == ops_map.end() && (" + " || ".join(syntax_only_conditions) + ")) {")
            lines.append("\t\treturn;")
            lines.append("\t}")
        if has_string_child_manual_transform:
            direct_conditions = [
                "choice_result.type == ParseResultType::IDENTIFIER",
                "choice_result.type == ParseResultType::KEYWORD",
                "choice_result.type == ParseResultType::STRING",
                "choice_result.type == ParseResultType::OPERATOR",
                "choice_result.type == ParseResultType::CHOICE",
                "choice_result.type == ParseResultType::LIST",
            ]
            lines.append("\tif (ops_entry == ops_map.end() && (" + " || ".join(direct_conditions) + ")) {")
            lines.append("\t\treturn;")
            lines.append("\t}")
        if self.is_parse_result_manual_choice_rule(rule_name, ast):
            lines.append("\tif (ops_entry == ops_map.end()) {")
            lines.append("\t\treturn;")
            lines.append("\t}")
        else:
            lines.append("\tif (ops_entry == ops_map.end()) {")
            lines.append(
                "\t\tthrow InternalException(\"No trampoline ops registered for rule '%s'\", choice_result.name);"
            )
            lines.append("\t}")
        lines.append(
            "\tstack.PushFrame(choice_result, *ops_entry->second, TransformFrameResultTarget(frame.frame_index, 0));"
        )
        lines.append("}")
        return lines

    def emit_sequence_rule(self, rule_name, ast):
        lines = self.emit_sequence_initialize(rule_name, ast)
        if self.is_manual_finalize_rule(rule_name):
            return lines
        plan = plan_trampoline_sequence_rule_with_terminals(
            ast,
            self.matcher_overrides,
            self.syntax_only_rules,
            allow_dynamic_stack_followers=True,
            allow_top_level_repeat=True,
        )
        cpp_type = self.cpp_type(rule_name)
        by_value = self.by_value(rule_name)

        lines.append("")
        lines.append(
            f"unique_ptr<TransformResultValue> PEGTransformerFactory::{finalize_name(rule_name)}(PEGTransformer &transformer, "
            f"TransformStack &stack, TransformStackFrame &frame) {{"
        )
        child_arg = self.auto_sequence_forward_child(rule_name, plan)
        if child_arg is not None:
            lines.extend(self.emit_sequence_forward_finalize_body(rule_name, plan, child_arg))
            return lines
        needs_list_pr = any(
            isinstance(
                arg,
                (
                    DirectParseArg,
                    DirectMatcherArg,
                    DirectOptionalArg,
                    DirectOptionalMatcherArg,
                    DirectOptionalPresenceArg,
                    DirectRepeatArg,
                    DirectListArg,
                    DirectOptionalListArg,
                    RepeatStackChild,
                    RequiredRepeatStackChild,
                    ListStackChild,
                    OptionalListStackChild,
                    TrailingOptionalStackChild,
                ),
            )
            for arg in plan.finalize_args
        )
        if needs_list_pr:
            lines.append("\tauto &list_pr = frame.parse_result.Cast<ListParseResult>();")
        lines.extend(self.emit_dynamic_child_count(plan))
        arg_names = []
        for arg in plan.finalize_args:
            if isinstance(arg, DirectParseArg):
                lines.append(f"\tauto {arg.var_name} = {arg.parse_expr};")
                arg_names.append(arg.var_name)
            elif isinstance(arg, DirectMatcherArg):
                lines.append(f"\tauto {arg.var_name} = {self.matcher_transform_expr(arg.rule_name, arg.parse_expr)};")
                arg_names.append(self.transform_arg_expr(arg.rule_name, arg.var_name))
            elif isinstance(arg, DirectOptionalArg):
                lines.append(f"\toptional<Identifier> {arg.var_name} {{}};")
                lines.append(f"\tauto &{arg.var_name}_opt = {arg.parse_expr}.Cast<OptionalParseResult>();")
                lines.append(f"\tif ({arg.var_name}_opt.HasResult()) {{")
                lines.append(
                    f"\t\t{arg.var_name} = {arg.var_name}_opt.GetResult().Cast<IdentifierParseResult>().identifier;"
                )
                lines.append("\t}")
                arg_names.append(arg.var_name)
            elif isinstance(arg, DirectOptionalMatcherArg):
                cpp_type = self.cpp_type(arg.rule_name)
                lines.append(f"\toptional<{cpp_type}> {arg.var_name} {{}};")
                lines.append(f"\tauto &{arg.var_name}_opt = {arg.parse_expr}.Cast<OptionalParseResult>();")
                lines.append(f"\tif ({arg.var_name}_opt.HasResult()) {{")
                child_expr = arg.result_expr_template.format(opt=f"{arg.var_name}_opt")
                value_expr = self.matcher_transform_expr(arg.rule_name, child_expr)
                lines.append(f"\t\t{arg.var_name} = {value_expr};")
                lines.append("\t}")
                arg_names.append(self.transform_arg_expr(arg.rule_name, arg.var_name))
            elif isinstance(arg, DirectOptionalPresenceArg):
                lines.append(f"\tbool {arg.var_name} {{}};")
                lines.append(f"\tauto &{arg.var_name}_opt = {arg.parse_expr}.Cast<OptionalParseResult>();")
                lines.append(f"\t{arg.var_name} = {arg.var_name}_opt.HasResult();")
                arg_names.append(arg.var_name)
            elif isinstance(arg, DirectRepeatArg):
                lines.append(f"\toptional<vector<Identifier>> {arg.var_name} {{}};")
                lines.append(f"\tauto &{arg.var_name}_opt = {arg.parse_expr}.Cast<OptionalParseResult>();")
                lines.append(f"\tif ({arg.var_name}_opt.HasResult()) {{")
                lines.append(f"\t\tvector<Identifier> {arg.var_name}_value;")
                lines.append(
                    f"\t\tauto &{arg.var_name}_repeat = {arg.var_name}_opt.GetResult().Cast<RepeatParseResult>();"
                )
                lines.append(f"\t\tfor (auto &{arg.var_name}_item : {arg.var_name}_repeat.GetChildren()) {{")
                lines.append(
                    f"\t\t\t{arg.var_name}_value.push_back({arg.var_name}_item.get().Cast<IdentifierParseResult>().identifier);"
                )
                lines.append("\t\t}")
                lines.append(f"\t\t{arg.var_name} = {arg.var_name}_value;")
                lines.append("\t}")
                arg_names.append(arg.var_name)
            elif isinstance(arg, DirectListArg):
                element_cpp_type = self.direct_matcher_cpp_type(arg.rule_name)
                lines.append(f"\tvector<{element_cpp_type}> {arg.var_name};")
                lines.append(f"\tauto {arg.var_name}_items = ExtractParseResultsFromList({arg.parse_expr});")
                lines.append(f"\tfor (auto &{arg.var_name}_item : {arg.var_name}_items) {{")
                if self.is_identifier_matcher(arg.rule_name):
                    lines.append(
                        f"\t\t{arg.var_name}.push_back("
                        f"{arg.var_name}_item.get().Cast<IdentifierParseResult>().identifier);"
                    )
                else:
                    lines.append(
                        f"\t\tauto {arg.var_name}_value = "
                        f"{self.direct_matcher_value_expr(arg.rule_name, f'{arg.var_name}_item.get()')};"
                    )
                    lines.append(
                        f"\t\t{arg.var_name}.push_back("
                        f"{self.direct_matcher_vector_push_expr(arg.rule_name, f'{arg.var_name}_value')});"
                    )
                lines.append("\t}")
                arg_names.append(arg.var_name)
            elif isinstance(arg, DirectOptionalListArg):
                element_cpp_type = self.direct_matcher_cpp_type(arg.rule_name)
                lines.append(f"\toptional<vector<{element_cpp_type}>> {arg.var_name} {{}};")
                lines.append(f"\tauto &{arg.var_name}_opt = {arg.parse_expr}.Cast<OptionalParseResult>();")
                lines.append(f"\tif ({arg.var_name}_opt.HasResult()) {{")
                child_expr = arg.result_expr_template.format(opt=f"{arg.var_name}_opt")
                lines.append(f"\t\tvector<{element_cpp_type}> {arg.var_name}_value;")
                lines.append(f"\t\tauto {arg.var_name}_items = ExtractParseResultsFromList({child_expr});")
                lines.append(f"\t\tfor (auto &{arg.var_name}_item : {arg.var_name}_items) {{")
                lines.append(
                    f"\t\t\tauto {arg.var_name}_item_value = "
                    f"{self.direct_matcher_value_expr(arg.rule_name, f'{arg.var_name}_item.get()')};"
                )
                lines.append(
                    f"\t\t\t{arg.var_name}_value.push_back("
                    f"{self.direct_matcher_vector_push_expr(arg.rule_name, f'{arg.var_name}_item_value')});"
                )
                lines.append("\t\t}")
                lines.append(f"\t\t{arg.var_name} = std::move({arg.var_name}_value);")
                lines.append("\t}")
                arg_names.append(arg.var_name)
            elif isinstance(arg, StackChild):
                var_name = arg.var_name
                slot_expr = self.adjusted_slot_expr(plan, arg.slot_idx)
                lines.append(f"\tauto {var_name} = frame.TakeResult<{self.cpp_type(arg.rule_name)}>({slot_expr});")
                arg_names.append(self.transform_arg_expr(arg.rule_name, var_name))
            elif isinstance(arg, OptionalStackChild):
                var_name = arg.var_name
                slot_expr = self.adjusted_slot_expr(plan, arg.slot_idx)
                lines.append(f"\toptional<{self.cpp_type(arg.rule_name)}> {var_name} {{}};")
                lines.append(f"\tif (frame.child_results[{slot_expr}]) {{")
                value_expr = f"frame.TakeResult<{self.cpp_type(arg.rule_name)}>({slot_expr})"
                lines.append(f"\t\t{var_name} = {value_expr};")
                lines.append("\t}")
                arg_names.append(self.transform_arg_expr(arg.rule_name, var_name))
            elif isinstance(arg, TrailingOptionalStackChild):
                var_name = arg.var_name
                lines.append(f"\toptional<{self.cpp_type(arg.rule_name)}> {var_name} {{}};")
                lines.append(f"\tauto &{var_name}_opt = {arg.parse_expr}.Cast<OptionalParseResult>();")
                lines.append(f"\tif ({var_name}_opt.HasResult()) {{")
                lines.append(f"\t\tauto slot_idx = frame.child_results.size() - 1;")
                value_expr = f"frame.TakeResult<{self.cpp_type(arg.rule_name)}>(slot_idx)"
                lines.append(f"\t\t{var_name} = {value_expr};")
                lines.append("\t}")
                arg_names.append(self.transform_arg_expr(arg.rule_name, var_name))
            elif isinstance(arg, RepeatStackChild):
                var_name = arg.var_name
                lines.append(f"\toptional<vector<{self.cpp_type(arg.rule_name)}>> {var_name} {{}};")
                lines.append("\tif (dynamic_child_count > 0) {")
                lines.append(f"\t\tvector<{self.cpp_type(arg.rule_name)}> {var_name}_value;")
                loop_end = f"{arg.slot_start} + dynamic_child_count"
                lines.append(f"\t\tfor (idx_t i = {arg.slot_start}; i < {loop_end}; i++) {{")
                lines.append(f"\t\t\t{var_name}_value.push_back(frame.TakeResult<{self.cpp_type(arg.rule_name)}>(i));")
                lines.append("\t\t}")
                lines.append(f"\t\t{var_name} = std::move({var_name}_value);")
                lines.append("\t}")
                arg_names.append(self.transform_arg_expr(arg.rule_name, var_name))
            elif isinstance(arg, OptionalListStackChild):
                var_name = arg.var_name
                lines.append(f"\toptional<vector<{self.cpp_type(arg.rule_name)}>> {var_name} {{}};")
                lines.append(f"\tauto &{var_name}_opt = {arg.parse_expr}.Cast<OptionalParseResult>();")
                lines.append(f"\tif ({var_name}_opt.HasResult()) {{")
                lines.append(f"\t\tvector<{self.cpp_type(arg.rule_name)}> {var_name}_value;")
                loop_end = f"{arg.slot_start} + dynamic_child_count"
                lines.append(f"\t\tfor (idx_t i = {arg.slot_start}; i < {loop_end}; i++) {{")
                lines.append(f"\t\t\t{var_name}_value.push_back(frame.TakeResult<{self.cpp_type(arg.rule_name)}>(i));")
                lines.append("\t\t}")
                lines.append(f"\t\t{var_name} = std::move({var_name}_value);")
                lines.append("\t}")
                arg_names.append(self.transform_arg_expr(arg.rule_name, var_name))
            elif isinstance(arg, RequiredRepeatStackChild):
                var_name = arg.var_name
                lines.append(f"\tvector<{self.cpp_type(arg.rule_name)}> {var_name};")
                loop_end = f"{arg.slot_start} + dynamic_child_count"
                lines.append(f"\tfor (idx_t i = {arg.slot_start}; i < {loop_end}; i++) {{")
                lines.append(f"\t\t{var_name}.push_back(frame.TakeResult<{self.cpp_type(arg.rule_name)}>(i));")
                lines.append("\t}")
                arg_names.append(self.transform_arg_expr(arg.rule_name, var_name))
            elif isinstance(arg, ListStackChild):
                var_name = arg.var_name
                lines.append(f"\tvector<{self.cpp_type(arg.rule_name)}> {var_name};")
                loop_end = f"{arg.slot_start} + dynamic_child_count"
                lines.append(f"\tfor (idx_t i = {arg.slot_start}; i < {loop_end}; i++) {{")
                lines.append(f"\t\t{var_name}.push_back(frame.TakeResult<{self.cpp_type(arg.rule_name)}>(i));")
                lines.append("\t}")
                arg_names.append(self.transform_arg_expr(arg.rule_name, var_name))
            else:
                raise NotImplementedError(f"unsupported finalize argument: {type(arg).__name__}")
        transform_args = ["transformer"] + arg_names
        if self.rule_types[rule_name].pass_location:
            transform_args.append("frame.parse_result.offset")
        lines.append(f"\tauto result = Transform{rule_name}({', '.join(transform_args)});")
        lines.append(f"\treturn {typed_result_expr(cpp_type, 'result', by_value)};")
        lines.append("}")
        return lines

    def emit_sequence_initialize(self, rule_name, ast):
        plan = plan_trampoline_sequence_rule_with_terminals(
            ast,
            self.matcher_overrides,
            self.syntax_only_rules,
            allow_dynamic_stack_followers=True,
            allow_top_level_repeat=True,
        )
        lines = []
        lines.append(
            f"void PEGTransformerFactory::{init_name(rule_name)}(PEGTransformer &transformer, TransformStack &stack, "
            f"TransformStackFrame &frame) {{"
        )
        frame_children = [arg for arg in plan.finalize_args if isinstance(arg, (StackChild, OptionalStackChild))]
        fixed_child_slots = max([arg.slot_idx + 1 for arg in frame_children], default=0)
        trailing_optional = plan.trailing_optional_stack_child
        dynamic_child = self.dynamic_child(plan)
        frame_children_before_dynamic = frame_children
        frame_children_after_dynamic = []
        if dynamic_child:
            frame_children_before_dynamic = [arg for arg in frame_children if arg.slot_idx < dynamic_child.slot_start]
            frame_children_after_dynamic = [arg for arg in frame_children if arg.slot_idx > dynamic_child.slot_start]
        logical_child_slots = fixed_child_slots
        if dynamic_child:
            logical_child_slots = max(logical_child_slots, dynamic_child.slot_start + 1)
        if frame_children or dynamic_child:
            lines.append("\tauto &list_pr = frame.parse_result.Cast<ListParseResult>();")
        if dynamic_child:
            if plan.list_child:
                list_child = plan.list_child
                lines.append(f"\tauto list_items = ExtractParseResultsFromList({list_child.parse_expr});")
                lines.append("\tauto dynamic_child_count = list_items.size();")
                if trailing_optional:
                    lines.append(
                        f"\tauto &{trailing_optional.var_name}_opt = {trailing_optional.parse_expr}.Cast<OptionalParseResult>();"
                    )
                    lines.append(
                        f"\tframe.ReserveChildSlots({logical_child_slots} + dynamic_child_count - 1 + ({trailing_optional.var_name}_opt.HasResult() ? 1 : 0));"
                    )
                    for stack_child in reversed(frame_children_after_dynamic):
                        self.emit_stack_child_push(lines, plan, stack_child)
                    lines.append(f"\tif ({trailing_optional.var_name}_opt.HasResult()) {{")
                    child_expr = trailing_optional.result_expr_template.format(opt=f"{trailing_optional.var_name}_opt")
                    lines.append(
                        f"\t\tstack.PushFrame({child_expr}, {ops_name(trailing_optional.rule_name)}, "
                        f"TransformFrameResultTarget(frame.frame_index, {logical_child_slots} + dynamic_child_count - 1));"
                    )
                    lines.append("\t}")
                else:
                    lines.append(f"\tframe.ReserveChildSlots({logical_child_slots} + dynamic_child_count - 1);")
                    for stack_child in reversed(frame_children_after_dynamic):
                        self.emit_stack_child_push(lines, plan, stack_child)
                lines.append("\tfor (idx_t i = list_items.size(); i > 0; i--) {")
                lines.append("\t\tauto child_idx = i - 1;")
                lines.append(
                    f"\t\tstack.PushFrame(list_items[child_idx].get(), {ops_name(list_child.rule_name)}, "
                    f"TransformFrameResultTarget(frame.frame_index, {list_child.slot_start} + child_idx));"
                )
                lines.append("\t}")
            elif plan.optional_list_child:
                list_child = plan.optional_list_child
                lines.append(f"\tauto &list_opt = {list_child.parse_expr}.Cast<OptionalParseResult>();")
                lines.append("\tidx_t dynamic_child_count = 0;")
                lines.append("\tif (list_opt.HasResult()) {")
                child_expr = list_child.result_expr_template.format(opt="list_opt")
                lines.append(f"\t\tauto list_items = ExtractParseResultsFromList({child_expr});")
                lines.append("\t\tdynamic_child_count = list_items.size();")
                lines.append(f"\t\tframe.ReserveChildSlots({logical_child_slots} + dynamic_child_count - 1);")
                for stack_child in reversed(frame_children_after_dynamic):
                    self.emit_stack_child_push(lines, plan, stack_child, "\t\t")
                lines.append("\t\tfor (idx_t i = list_items.size(); i > 0; i--) {")
                lines.append("\t\t\tauto child_idx = i - 1;")
                lines.append(
                    f"\t\t\tstack.PushFrame(list_items[child_idx].get(), {ops_name(list_child.rule_name)}, "
                    f"TransformFrameResultTarget(frame.frame_index, {list_child.slot_start} + child_idx));"
                )
                lines.append("\t\t}")
                lines.append("\t} else {")
                lines.append(f"\t\tframe.ReserveChildSlots({logical_child_slots} - 1);")
                for stack_child in reversed(frame_children_after_dynamic):
                    self.emit_stack_child_push(lines, plan, stack_child, "\t\t")
                lines.append("\t}")
            elif plan.required_repeat_child:
                repeat_child = plan.required_repeat_child
                lines.append(f"\tauto &repeat_pr = {repeat_child.parse_expr}.Cast<RepeatParseResult>();")
                lines.append("\tauto repeat_children = repeat_pr.GetChildren();")
                lines.append("\tauto dynamic_child_count = repeat_children.size();")
                if trailing_optional:
                    lines.append(
                        f"\tauto &{trailing_optional.var_name}_opt = {trailing_optional.parse_expr}.Cast<OptionalParseResult>();"
                    )
                    lines.append(
                        f"\tframe.ReserveChildSlots({logical_child_slots} + dynamic_child_count - 1 + ({trailing_optional.var_name}_opt.HasResult() ? 1 : 0));"
                    )
                    for stack_child in reversed(frame_children_after_dynamic):
                        self.emit_stack_child_push(lines, plan, stack_child)
                    lines.append(f"\tif ({trailing_optional.var_name}_opt.HasResult()) {{")
                    child_expr = trailing_optional.result_expr_template.format(opt=f"{trailing_optional.var_name}_opt")
                    lines.append(
                        f"\t\tstack.PushFrame({child_expr}, {ops_name(trailing_optional.rule_name)}, "
                        f"TransformFrameResultTarget(frame.frame_index, {logical_child_slots} + dynamic_child_count - 1));"
                    )
                    lines.append("\t}")
                else:
                    lines.append(f"\tframe.ReserveChildSlots({logical_child_slots} + dynamic_child_count - 1);")
                    for stack_child in reversed(frame_children_after_dynamic):
                        self.emit_stack_child_push(lines, plan, stack_child)
                lines.append("\tfor (idx_t i = repeat_children.size(); i > 0; i--) {")
                lines.append("\t\tauto child_idx = i - 1;")
                lines.append(
                    f"\t\tstack.PushFrame(repeat_children[child_idx].get(), {ops_name(repeat_child.rule_name)}, "
                    f"TransformFrameResultTarget(frame.frame_index, {repeat_child.slot_start} + child_idx));"
                )
                lines.append("\t}")
            else:
                repeat_child = plan.repeat_child
                lines.append(f"\tauto &repeat_opt = {repeat_child.parse_expr}.Cast<OptionalParseResult>();")
                lines.append("\tidx_t dynamic_child_count = 0;")
                lines.append("\tif (repeat_opt.HasResult()) {")
                lines.append("\t\tauto &repeat_pr = repeat_opt.GetResult().Cast<RepeatParseResult>();")
                lines.append("\t\tauto repeat_children = repeat_pr.GetChildren();")
                lines.append("\t\tdynamic_child_count = repeat_children.size();")
                lines.append(f"\t\tframe.ReserveChildSlots({logical_child_slots} + dynamic_child_count - 1);")
                for stack_child in reversed(frame_children_after_dynamic):
                    self.emit_stack_child_push(lines, plan, stack_child, "\t\t")
                lines.append("\t\tfor (idx_t i = repeat_children.size(); i > 0; i--) {")
                lines.append("\t\t\tauto child_idx = i - 1;")
                lines.append(
                    f"\t\t\tstack.PushFrame(repeat_children[child_idx].get(), {ops_name(repeat_child.rule_name)}, "
                    f"TransformFrameResultTarget(frame.frame_index, {repeat_child.slot_start} + child_idx));"
                )
                lines.append("\t\t}")
                lines.append("\t} else {")
                lines.append(f"\t\tframe.ReserveChildSlots({logical_child_slots} - 1);")
                for stack_child in reversed(frame_children_after_dynamic):
                    self.emit_stack_child_push(lines, plan, stack_child, "\t\t")
                lines.append("\t}")
        else:
            lines.append(f"\tframe.ReserveChildSlots({fixed_child_slots});")
        for stack_child in reversed(frame_children_before_dynamic):
            self.emit_stack_child_push(lines, plan, stack_child)
        lines.append("}")
        return lines

    def matcher_transform_expr(self, rule_name, parse_expr):
        matcher = self.matcher_overrides[rule_name].matcher
        if matcher == "string_literal":
            return f"TransformStringLiteral(transformer, {parse_expr})"
        if matcher == "number_literal":
            return f"TransformNumberLiteral(transformer, {parse_expr})"
        if matcher == "operator":
            return f"{parse_expr}.Cast<OperatorParseResult>().operator_token"
        if matcher in ("identifier", "identifier_string"):
            return f"{parse_expr}.Cast<IdentifierParseResult>().identifier.GetIdentifierName()"
        raise NotImplementedError(f"unsupported matcher override: {matcher}")


def main():
    arg_parser = argparse.ArgumentParser(description="Generate trampoline-style transformer wrappers.")
    arg_parser.add_argument("--write", action="store_true", help="Write generated files to disk.")
    arg_parser.add_argument("--report", action="store_true", help="Print per-rule trampoline generation status.")
    arg_parser.add_argument(
        "--grammar",
        action="append",
        default=None,
        help="Grammar file to process for reports/previews. Defaults to all statement grammar files.",
    )
    args = arg_parser.parse_args()

    all_grammar_files = discover_grammar_files()
    grammar_files = sorted(set(args.grammar or all_grammar_files))
    rules = load_grammar_rules(grammar_files)
    all_rules = load_grammar_rules(all_grammar_files)
    config_file = type_dir / "transformer_trampoline.yml"
    rule_config = load_transformer_trampoline_config(config_file, all_rules.keys())
    grammar_types_file = type_dir / "grammar_types.yml"
    rule_types, excluded_rules = load_grammar_types(grammar_types_file)
    matcher_overrides = load_matcher_overrides(grammar_types_file)
    validate_manual_transform_signatures(all_grammar_files, grammar_types_file, rule_types, excluded_rules)

    emitter = UseGramPreviewEmitter(rules, rule_types, excluded_rules, matcher_overrides, rule_config)
    emitter.validate_grammar_types()
    if args.write:
        if args.grammar is not None:
            arg_parser.error("--write always generates all statement grammar files")
        write_hpp(emitter.emit_header_declarations())
        write_cpp(emitter.emit_source())
    elif args.report:
        print(f"grammar files: {', '.join(grammar_files)}")
        print(emitter.emit_report())
    else:
        print(f"// grammar files: {', '.join(grammar_files)}")
        print(f"// rules: {len(rules)}")
        print(emitter.emit())


def write_hpp(declarations):
    hpp_path = include_peg_dir / "peg_transformer.hpp"
    content = hpp_path.read_text()
    start_idx = content.find(TRAMPOLINE_START_BLOCK)
    if start_idx == -1:
        raise RuntimeError(f"Could not find START GENERATED TRAMPOLINE RULES marker in {hpp_path}")
    end_idx = content.find(TRAMPOLINE_END_BLOCK, start_idx + len(TRAMPOLINE_START_BLOCK))
    if end_idx == -1:
        raise RuntimeError(f"Could not find END GENERATED TRAMPOLINE RULES marker in {hpp_path}")
    block_end = end_idx + len(TRAMPOLINE_END_BLOCK)
    generated_block = TRAMPOLINE_START_BLOCK + declarations + TRAMPOLINE_END_BLOCK
    hpp_path.write_text(content[:start_idx] + generated_block + content[block_end:])
    print(f"Updated {hpp_path}")


def write_cpp(source):
    cpp_path = transformer_dir / "transform_generated_trampoline.cpp"
    cpp_path.write_text(source + "\n")
    print(f"Updated {cpp_path}")


if __name__ == "__main__":
    main()
